package prueba_grupal;

public class Trupalla extends Carro 
{

	int armadura;
	String conductor;
	
	public String getConductor() 
	{
		return conductor;
	}
	public void setConductor() 
	{
		conductor = super.b;
	}

	public Trupalla(int x, int y, int a, String b) 
	{
		super(x, y, a, b);
	}
	public int getArmadura() 
	{
		return armadura;
	}
	public void setArmadura() 
	{
		armadura = super.a;
	}

}
